setCookie('LP','{"courses-layout":"grid"}');

function setCookie(key, value) {
  var expires = new Date();
  expires.setTime(expires.getTime() + (value * 24 * 60 * 60 * 1000));
  document.cookie = key + '=' + value + ';expires=' + expires.toUTCString();
}
